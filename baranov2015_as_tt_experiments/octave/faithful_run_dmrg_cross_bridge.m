function faithful_run_dmrg_cross_bridge(config_path)
cfg = jsondecode(fileread(config_path));

orig_dir = pwd();
cleanup_obj = onCleanup(@() cd(orig_dir));

cd(cfg.tt_toolbox_root);
setup;
cd(orig_dir);
addpath(cfg.bridge_dir);

if isfield(cfg, 'random_state') && ~isempty(cfg.random_state)
    try
        rng(double(cfg.random_state));
    catch
        rand('seed', double(cfg.random_state));
        randn('seed', double(cfg.random_state));
    end
end

n = double(cfg.shape(:));
d = numel(n);
fun = @(ind) faithful_octave_http_eval(ind, cfg.oracle_bridge_dir);
[y, info] = faithful_dmrg_cross_budgeted( ...
    d, ...
    n, ...
    fun, ...
    double(cfg.tol), ...
    'vec', true, ...
    'verb', logical(cfg.verbose), ...
    'nswp', double(cfg.n_iter_max), ...
    'maxr', double(cfg.rank_cap) ...
);

dense_tensor = full(y);
tt_ranks = double(y.r(:))';
stopped_by_budget = logical(info.stopped_by_budget);
converged = logical(info.converged);
returned_checkpoint = false;
if isfield(info, 'returned_checkpoint')
    returned_checkpoint = logical(info.returned_checkpoint);
end
budget_message = '';
if isfield(info, 'message')
    budget_message = char(info.message);
end
sweeps_completed = double(info.sweeps_completed);
last_block = double(info.last_block);

save( ...
    '-mat7-binary', ...
    cfg.output_mat, ...
    'dense_tensor', ...
    'tt_ranks', ...
    'stopped_by_budget', ...
    'converged', ...
    'returned_checkpoint', ...
    'budget_message', ...
    'sweeps_completed', ...
    'last_block' ...
);
end
